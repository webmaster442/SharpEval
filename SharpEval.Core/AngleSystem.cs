namespace SharpEval.Core;

/// <summary>
/// Represents the Angle system for trigonometric functions
/// </summary>
public enum AngleSystem
{
    /// <summary>
    /// Angle in degrees
    /// </summary>
    Deg = 0,
    /// <summary>
    /// Angle in radians
    /// </summary>
    Rad = 1,
    /// <summary>
    /// Angle in gradians
    /// </summary>
    Grad = 2,
}
